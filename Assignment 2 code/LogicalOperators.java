
public class LogicalOperators {
	
	    public static void main(String[] args) {
	        boolean expr1 = true;
	        boolean expr2 = false;

	        System.out.println("expr1 && expr2: " + (expr1 && expr2));
	        System.out.println("expr1 || expr2: " + (expr1 || expr2));
	        System.out.println("!expr1: " + (!expr1));
	    }
	}


